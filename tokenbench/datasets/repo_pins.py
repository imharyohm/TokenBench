"""Pinned-repo manifest. Each entry is a content-addressed snapshot.

The snapshot_sha256 is filled in after `scripts/snapshot_repos.py` runs once;
on every subsequent run, the script verifies the on-disk hash matches.
This is what gives the benchmark byte-level reproducibility.

Hashing rule (Chunk 4): hash the source tree as cloned-at-commit, NOT
literal on-disk state. Tool byproducts that mutate the tree
(graphify-out/, __pycache__/, .pytest_cache/) are excluded so the hash
stays stable when downstream tools run against the snapshot.
"""

from __future__ import annotations

import hashlib
from dataclasses import dataclass
from pathlib import Path
from typing import Optional

# Directories/files generated by tooling against the snapshot tree.
# These never carry source content; excluding them keeps snapshot_sha256
# stable across graphify builds, pytest runs, etc.
SNAPSHOT_EXCLUDE_DIRS = frozenset({
    ".git",
    "graphify-out",
    "__pycache__",
    ".pytest_cache",
    ".mypy_cache",
    ".ruff_cache",
    ".tox",
    ".venv",
    ".DS_Store",
})


def _is_excluded(path_parts: tuple[str, ...]) -> bool:
    return any(part in SNAPSHOT_EXCLUDE_DIRS for part in path_parts)


def hash_tree(path: Path) -> str:
    """Stable sha256 over (relative_path, content) pairs sorted by path.

    Excludes SNAPSHOT_EXCLUDE_DIRS at any depth. This is the single
    canonical hashing function — `scripts/snapshot_repos.py` and
    `scripts/verify_snapshot.py` both call it so they cannot drift.
    """
    h = hashlib.sha256()
    for f in sorted(p for p in path.rglob("*") if p.is_file()):
        rel = f.relative_to(path)
        if _is_excluded(rel.parts):
            continue
        rel_bytes = rel.as_posix().encode()
        h.update(len(rel_bytes).to_bytes(4, "big"))
        h.update(rel_bytes)
        data = f.read_bytes()
        h.update(len(data).to_bytes(8, "big"))
        h.update(data)
    return h.hexdigest()


@dataclass(frozen=True)
class RepoPin:
    short_id: str
    url: str
    commit: str
    license: str
    language: str
    snapshot_sha256: Optional[str] = None  # filled after first snapshot


